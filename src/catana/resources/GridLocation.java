package catana.resources;

/**
 * represents a specific hex somewhere on the map
 * @author willvdb
 *
 */

public class GridLocation {
	
	int x;
	int y;
	
	public void setX(int x) {
		this.x = x;
	}

	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	

}
